import { Slide, ToastContainer, toast } from "react-toastify";

export const toastSuccess = (message) => {
  return toast.success(message ? message : "Successfyli ", {
    position: "bottom-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};

